pw.Widget ListTile(
  pw.Context context, {
  required String title,
  String subtitle = '',
  required double trailing,
  bool bold = false,
}) {
  return pw.Padding(
    padding: pw.EdgeInsets.all(25),
    child: pw.Row(
      children: [
        pw.Text('$trailing ريال', style: pw.TextStyle(font: arabicFont, fontSize: bold ? 17 : 15)), // style: pw.Theme.of(context).header)),
        pw.Spacer(),
        pw.Column(
          crossAxisAlignment: pw.CrossAxisAlignment.end,
          children: [
            pw.Text(title, style: pw.TextStyle(font: arabicFont, fontSize: bold ? 17 : 15)), // style: pw.Theme.of(context).header)),
